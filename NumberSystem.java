public class NumberSystem {
    public static void main(String[] args) {
        int decvalue=015;
        int binvalue=0b1011;
        int octvalue=014;
        int hexvalue=0x78;

        System.out.println("Decimal is : "+decvalue);
        System.out.println("Binary is : "+binvalue);
        System.out.println("octal is : "+octvalue);
        System.out.println("hexadecimal is : "+hexvalue);
        
    }
}
